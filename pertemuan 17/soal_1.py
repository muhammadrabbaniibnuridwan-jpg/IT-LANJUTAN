# Tuple berisi nama hari
hari = ("Senin", "Selasa", "Rabu", "Kamis", "Jumat", "Sabtu", "Minggu")

# Tuple dengan satu elemen (harus pakai koma!)
angka_ganjil = (7,)

# Cetak isi dan tipe data
print(hari)
print(type(hari))        # <class 'tuple'>

print(angka_ganjil)
print(type(angka_ganjil))  # <class 'tuple'>


